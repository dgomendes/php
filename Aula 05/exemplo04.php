<?php
    $a = 34;
    $b = 35;

    /* <=> é chamado de SpaceShip
    Verifica com um unico operador se as
    variáveis quando comparadas são 
    menores, iguais ou maiores a primeira
    informada*
    
    Caso seje menor, o comando irá retornar -1
    Caso maior, retorna 1,
    e caso seje igual, o retorno é 0;
    */
    var_dump($a <=> $b); 


    
